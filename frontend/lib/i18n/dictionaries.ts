export type Locale = keyof typeof dictionaries;

// The `en` and `ar` dictionaries are structurally identical but carry their
// own literal string types (via `as const`), so plain `typeof ...["en"]`
// rejects the `ar` object. Widen every leaf to `string` so both conform.
type Widen<T> = T extends string
  ? string
  : T extends (...args: infer A) => infer R
  ? (...args: A) => R
  : T extends readonly (infer U)[]
  ? readonly Widen<U>[]
  : T extends object
  ? { [K in keyof T]: Widen<T[K]> }
  : T;

export type Dict = Widen<(typeof dictionaries)["en"]>;
